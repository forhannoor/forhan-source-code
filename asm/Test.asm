TITLE 8255

.MODEL SMALL
;.STACK 100H
.DATA

CNT3	EQU	3FD6H;
BPORT3 	EQU	3FD2H;

.CODE

MAIN PROC

START: MOV SP,4000H;
       MOV AL,90H;
       
       MOV DX,CNT3
       OUT DX,AL
       MOV AL,0FH
       MOV DX,BPORT3
J1:
   OUT DX,AL
   MOV CX,0FFFFH
   LOOP $
   NOT AL
jmp j1

MAIN ENDP
END START