    public void addToTable(SalesLineItem sli){
        String id = sli.getProduct().getId();   
        String name = sli.getProduct().getName();
        int quantity = sli.getQuantity();
        double subTotal = sli.getSubTotal();
        sales_line_items_table.setValueAt(id, UI.rowCount, 0);
        sales_line_items_table.setValueAt(name, UI.rowCount, 1);
        sales_line_items_table.setValueAt(quantity, UI.rowCount, 2);
        sales_line_items_table.setValueAt(subTotal, UI.rowCount, 3);
        UI.rowCount += 1;
        total.setText(Double.toString(this.s.getTotal()));
    }
    
