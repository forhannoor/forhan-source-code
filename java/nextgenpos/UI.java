    /* finish current sale and display total */
    private void finish_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finish_buttonActionPerformed
        double t = 0 ;
        
        try{
            if(UI.active_strategy == 0){        /* no pricing strategy */
                t = this.s.getTotal();
            }
            
            else if(UI.active_strategy == 1){   /* percentage discount strategy */
                PercentDiscountPricingStrategy obj = new PercentDiscountPricingStrategy(pf.getPercent());
                t = obj.getTotal(this.s);
            }
            
            else if(UI.active_strategy == 2){   /* threshold discount strategy */
                AbsoluteDiscountOverThresholdPricingStrategy obj = new AbsoluteDiscountOverThresholdPricingStrategy(pf.getDiscount(), pf.getThreshold());
                t = obj.getTotal(this.s);
            }
            
            total_after_discount.setText(Double.toString(t));
            double discount = Double.parseDouble(total.getText()) -Double.parseDouble(total_after_discount.getText());
            discount_amount.setText(Double.toString(discount));
            //total.setText(Double.toString(t));
            this.s = null;   /* destroy sale object */
        } catch(NullPointerException npe){
            System.out.println("Cannot finish sale that has not been started!");
        }
        
        this.pf = null;
        this.clearTable();
        this.clearInputFields();
    }//GEN-LAST:event_finish_buttonActionPerformed

