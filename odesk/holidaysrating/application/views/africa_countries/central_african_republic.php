<h1>Climate</h1>
<p>The climate is generally tropical. The northern areas are subject to harmattan winds, which are hot, dry, and dusty. The northern regions have been subject to desertification, and the Northeast is desert. The remaining of the country is prone to flooding from nearby rivers.</p>

<h1>Flora and Fauna</h1>
<p>Most of the country has savanna vegetation, which is a grassland distributed with trees. Open grassland is found in the North, and a dense rain forest covers the major part of the Southwestern area. Commercially valuable trees include the sapele Mahogany and the Obeche. As far as fauna is concerned, almost every animal of the African Tropics is found in Central African Republic. <?php echo anchor('africa_national_parks/car_parks', '(visit National Parks)') ?></p>

