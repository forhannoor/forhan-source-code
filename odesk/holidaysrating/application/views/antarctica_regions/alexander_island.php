<h1>Introduction</h1>
<p><strong>Alexander Island</strong>, is the largest island of Antarctica and is the second largest uninhabited island in the world, after Devon Island. It lies in the Bellingshausen Sea, west of <?php echo anchor('antarctica_regions/palmer_land', 'Palmer Land') ?>, in the Antarctic Peninsula which is separated by Marguerite Bay and George VI Sound. George VI Ice Shelf entirely fills George VI Sound and connects Alexander island to Palmer Land. Alexander Island is about 240 miles (390 km) long in a north-south direction, 50 miles (80 km) wide in the north, and 150 miles (240 km) wide in the south.</p>
<p>This Arctic Island is predominantly ice covered. There are some exposed nunataks and a few free of ice areas, for example Ablation Point Massif.</p>
