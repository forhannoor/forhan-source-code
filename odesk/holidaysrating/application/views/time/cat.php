<p>Central Africa Time, or CAT, is a time zone used in central and southern Africa. Central Africa Time is two hours ahead of Coordinated Universal Time (<?php echo anchor('time/utc', 'UTC ') ?>+02:00), which is the same as the adjacent South Africa Standard Time and also the same as Eastern European Time.
<br /><br />
As this time zone is predominantly in the equatorial region, there is no significant change in day length throughout the year, so daylight saving time is not observed.</p>



