<p>West Africa Time, or <?php echo anchor('time/wat', 'WAT ') ?>, is a time zone used in west-central Africa 
(though not in countries west of Benin, which instead use <?php echo anchor('time/utc', 'UTC ') ?>). The zone is one hour ahead of <?php echo anchor('time/utc', 'Coordinated Universal Time') ?>(UTC+01), 
which makes it the same as Central European Time. Note that it does not include most of the countries that make up the West Africa region (these use UTC).</p>


